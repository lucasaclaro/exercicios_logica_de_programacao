#Escreva um algoritmo para ler as dimensões de um retângulo e calcule sua área.

heigth = int(input('Digite a medida da altura do retângulo: '))
base = int(input('Digite a medida da base do retângulo: '))
area = heigth * base
print(f'A área do retângulo, conforme a medida dos lados digitados ({heigth} e {base}) é {area}.')